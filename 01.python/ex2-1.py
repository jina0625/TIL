# 논리 연산자
    # not : 참과 거짓을 반대로 바꿀 때 사용
    # and : 연산자 왼쪽 값과 오른쪽 값이 모두 참일 때 -> True
            # 그 외 -> False
    # or : 연산자 왼쪽 값과 오른쪽 값 하나라도 참이면 -> True
            # 두 개가 모두 거짓 -> False
x = True
a = (0 > 7) or (6 > 9)
b = (2 > 3) and (1 > 0)
c = not(1 > 0)
d = (4 > 3) and x
print(a)
print(b)
print(c)
print(d)