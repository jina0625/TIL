# 비교 연산자
    # 두 가지 값을 비교하여 연산
    # 결과는 항상 True / False 값만 가짐
    # 문자열도 비교 연산 가능
        # 한글 -> 사전 순서대로 (가나다) 앞에 있는 것이 작은 값 가짐
    # < , > , <= , >= , == , !=
    # 범위 구하기 : from < 변수 < to
