# 문자열 인덱싱
    # 문자열 내부 문자 하나를 선택하는 연산자
    # [] 대괄호 안에 선택할 문자 위치 지정(인덱스)
    # 0부터 시작
    # 음수 인덱스는 -1부터 시작
print("안녕하세요"[1])
print("안녕하세요"[-1])
print("안녕하세요"[-4])
print("안녕하세요"[3])

print()

# 문자열 슬라이싱
    # 문자열 특정 범위 선택 시 사용하는 연산자
    # [start : stop : step]
        # start : 추출하고자 하는 첫 인덱스
        # stop : 추출하고자 하는 마지막 인덱스에 1 더한 값
        # step : 추출 조건, 생략 가능
a = "Hello Python, 반가워요"
print(a[1])
print(a[0:5])
print(a[14:18])
print(a[-4:])
print(a[-1:-4:-1])
print(a[::-1])
print(a[::2])

print()

